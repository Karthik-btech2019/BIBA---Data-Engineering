# String Functions
print("String Functions")
s="hexaware"
print(s.capitalize()) # Returns the string value with first letter Upper case
print(len(s)) # Returns the length of the string
print(s.isalpha()) # Returns True if all the values in string are alphabetic characters
print(s.islower()) # Returns True if all the values in string are in lower case
print(s.upper()) # Returns with the string value with upper case