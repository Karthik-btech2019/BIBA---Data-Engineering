# Number Functions in python
from filecmp import cmp
from math import ceil, log, sqrt
print("Number Functions")
print(abs(22.6)) # Returns the absolute value
print(ceil(17.3)) # Returns the ceilimg value 
print(cmp(10,20)) # Retruns 0 if values are equal, returns 1 if value1 > value2 and  returns -1 if value1 < value2
print(log(2)) # Returns the logarithmic value
print(max(12,31)) # Returns the max value with comparison
print(min(18,9)) # Returns the min value with comparison
print(pow(2,6)) # Returns the value 2 to the power of 6
print(sqrt(2)) # returns the square root value