#Operators in python
num1=5
num2=7
print("Arithmetic Operators")
print(num1+num2) # addition of two numbers
print(num1-num2) # subtraction of two numbers
print(num1*num2) # multiplication of two numbers
print(num1/num2) # division of two numbers
print(num1//num2) # floor division of two numbers
print(num1%num2) # modulus of two numbers