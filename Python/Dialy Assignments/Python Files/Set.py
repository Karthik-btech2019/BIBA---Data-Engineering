# Sets in Python
print("Set in Python")
myset = {'BMW','Audi','Benz','Rolls Royce','BMW','Benz','Volvo'}
print(myset) # Printing the whole set
print("Length of my set is: ",len(myset)) # Printing the length of the set
myset.remove("Audi") # Removing the value Audi from the set
print("My Set after removing the value Audi:",myset)
myset.add("Scania") # Adding the value Scania to the set